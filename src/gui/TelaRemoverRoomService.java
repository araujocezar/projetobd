    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String c = entryCodigo.getText();
        if(c.equals("")){
            JOptionPane.showMessageDialog(null, "Preencha o campo código!");
        }
        else{
            try{
                int codigo = Integer.parseInt(c);
                ServicoQuarto s = fachada.buscarServico(codigo);
                fachada.removerOpcao(s);
                JOptionPane.showMessageDialog(null, "Remoção Realizada!");
                this.dispose();
            }
            catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null, "Informe um código válido!");
            } catch (ItemInexistenteException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

